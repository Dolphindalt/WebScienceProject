    <div class='container-content'>
        <h4 style='margin-top: 10px;'>What is 3leaf?</h4>
        <p>
            3leaf is an image board site, inspired by Futaba Channel, and constrained to additional design requirements. 
            Users create an account to post on boards dedicated to various subjects.
        </p>
        <h4>What should I know before posting?</h4>
        <p>
            Read the contents of the <a href='index.php?rules'>rules</a> and this FAQ. 
            Failing to follow the rules may result in a ban or your post being deleted.
        </p>
        <h4>Do I need to post an image?</h4>
        <p>
            Image posting is encouraged but not required unless a new thread is being posted. New threads require 
            an image in the first post.
        </p>
        <h4>How do I reference a post from my own post?</h4>
        <p>
            To reference a post, specify the post ID prefixed by two less than signs. Example: <a class='text-link'>>>143</a>. 
            The reference will be made once the post is submitted. References that point to a post that does not exist or has  
            been deleted will show up with a strike though. Example: <a class='text-link' style='text-decoration:line-through;'>>>6342</a>. 
            Posts that reference a post have their IDs shown on the referenced post.
        </p>
        <h4>How do I report posts?</h4>
        <p>
            I have not implemented this yet.
        </p>
        <h4>Why is my post gone?</h4>
        <p>
            Posts eventually get archived and deleted as threads slide off the page and become inactive. A board allows for 
            10 active threads at a time. Your post may have been deleted due to a rule violation. Please do not 
            reupload a post you suspect was deleted.
        </p>
        <h4>Why is my thread not bumping to the top of the page?</h4>
        <p>
            Threads that reach the post limit of 10 will no longer bump to the top of the page after a reply. This is to limit the 
            lifetime of a thread such that it does not dominate the thread page indefinitely.
        </p>
    </div>
</div>